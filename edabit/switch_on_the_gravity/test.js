
const solution = require('./solution');


test("Mirror Array", () => {
    expect(solution.switchGravityOn([
        ["-", "#", "#", "-"],
        ["-", "-", "-", "-"],
        ["-", "-", "-", "-"],
        ["-", "-", "-", "-"]
    ])).toStrictEqual([
        ["-", "-", "-", "-"],
        ["-", "-", "-", "-"],
        ["-", "-", "-", "-"],
        ["-", "#", "#", "-"]
    ]);
    expect(solution.switchGravityOn([
        ["-", "#", "#", "-"],
        ["-", "-", "#", "-"],
        ["-", "-", "-", "-"],
    ])).toStrictEqual([
        ["-", "-", "-", "-"],
        ["-", "-", "#", "-"],
        ["-", "#", "#", "-"]
    ]);
    expect(solution.switchGravityOn([
        ["-", "#", "#", "#", "#", "-"],
        ["#", "-", "-", "#", "#", "-"],
        ["-", "#", "-", "-", "-", "-"],
        ["-", "-", "-", "-", "-", "-"]
    ])).toStrictEqual([
        ["-", "-", "-", "-", "-", "-"],
        ["-", "-", "-", "-", "-", "-"],
        ["-", "#", "-", "#", "#", "-"],
        ["#", "#", "#", "#", "#", "-"]
    ]);
    expect(solution.switchGravityOn([
        ["-", "#", "#", "#", "#", "-"],
        ["#", "-", "-", "#", "#", "-"],
        ["-", "#", "-", "-", "-", "-"],
        ["-", "#", "-", "#", "-", "-"]
    ])).toStrictEqual([
        ["-", "-", "-", "-", "-", "-"],
        ["-", "#", "-", "#", "-", "-"],
        ["-", "#", "-", "#", "#", "-"],
        ["#", "#", "#", "#", "#", "-"]
    ]);
    expect(solution.switchGravityOn([
        ["-", "#", "#", "-"],
        ["-", "-", "#", "-"],
        ["#", "#", "-", "-"],
    ])).toStrictEqual([
        ["-", "-", "-", "-"],
        ["-", "#", "#", "-"],
        ["#", "#", "#", "-"]
    ]);
    expect(solution.switchGravityOn([
        ["#"],
        ["-"],
        ["#"],
        ["-"]
    ])).toStrictEqual([
        ["-"],
        ["-"],
        ["#"],
        ["#"]
    ]);
    expect(solution.switchGravityOn([
        ["#"],
        ["#"],
        ["#"],
        ["#"]
    ])).toStrictEqual([
        ["#"],
        ["#"],
        ["#"],
        ["#"]
    ]);
});
